    private void BuatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BuatMouseClicked
        // TODO add your handling code here:
        String namaDokter = NamaDokter.getText();
        String spesialisDokter = Spesialis1.getText();
        String telepon = Telepon.getText();
        String emailDokter = Email.getText();
        String user = username.getText();
        String pass = password.getText();

        PreparedStatement ps;
        String query
                = "INSERT INTO `dokter`(`id_dokter`, `nama dokter`, `spesialis`,`noTelp`,`email`,`username`,`password`) VALUES (?,?,?,?,?,?,?)";
        try {
            lastIndexDokter++;
            System.out.println(lastIndexDokter);
            ps = Connection.getConnection().prepareStatement(query);
            System.out.println(lastIndexDokter);
            ps.setInt(1, lastIndexDokter);
            ps.setString(2, namaDokter);
            ps.setString(3, spesialisDokter);
            ps.setString(4, telepon);
            ps.setString(5, emailDokter);
            ps.setString(6, user);
            ps.setString(7, pass);

            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Berhasil");
                this.setVisible(false);
                if (spesialisDokter.equals("Penyakit Dalam")) {
                    Spesialis_penyakitDalam newKandungan = new Spesialis_penyakitDalam(lastIndexDokter, namaDokter, spesialisDokter, telepon, emailDokter, user, pass);
                    Spesialis_penyakitDalam.add(newKandungan);
                } else if (spesialisDokter.equals("Umum")) {
                    Spesialis_umum newUmum = new Spesialis_umum(lastIndexDokter, namaDokter, spesialisDokter, telepon, emailDokter, user, pass);
                    Spesialis_umum.add(newUmum);
                } else if (spesialisDokter.equals("Kandungan")) {
                    Spesialis_kandungan newKandungan = new Spesialis_kandungan(lastIndexDokter, namaDokter, spesialisDokter, telepon, emailDokter, user, pass);
                    Spesialis_kandungan.add(newKandungan);
                } else if (spesialisDokter.equals("Mata")) {
                    Spesialis_mata newMata = new Spesialis_mata(lastIndexDokter, namaDokter, spesialisDokter, telepon, emailDokter, user, pass);
                    Spesialis_mata.add(newMata);
                } else if (spesialisDokter.equals("THT")) {
                    Spesialis_tht NewTHT = new Spesialis_tht(lastIndexDokter, namaDokter, spesialisDokter, telepon, emailDokter, user, pass);
                    Spesialis_tht.add(NewTHT);
                }
                new Dokter().setVisible(true);
            } else {
                System.out.println("Gabisa!");
            }

        } catch (SQLException ex) {
            System.err.println(ex);
        }

    }//GEN-LAST:event_BuatMouseClicked

