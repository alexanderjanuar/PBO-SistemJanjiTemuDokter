    private void LihatDokter() {
        DefaultTableModel tableDokter = new DefaultTableModel();
        tableDokter.addColumn("ID Dokter");
        tableDokter.addColumn("Nama Dokter");
        tableDokter.addColumn("Spesialis");
        tableDokter.addColumn("Email");
        tableDokter.addColumn("No Telepon");

        try {
            String sql = "SELECT dokter.id_dokter, dokter.`nama dokter`, dokter.spesialis, dokter.email, dokter.noTelp FROM dokter";
            java.sql.Connection conn = (java.sql.Connection) Connection.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet res = stmt.executeQuery(sql);
            while (res.next()) {
                tableDokter.addRow(new Object[]{res.getInt(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5)});
            }
            tabelDataDokter.setModel(tableDokter);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error : " + e.getMessage());
        }
    }
